class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        dict1 = {}
        for i , num in enumerate(nums):
            x = target - num
            if x in dict1:
                return[dict1[x], i]
            dict1[num] = i

